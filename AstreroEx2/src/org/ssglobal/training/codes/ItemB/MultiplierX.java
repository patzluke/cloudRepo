package org.ssglobal.training.codes.ItemB;

public class MultiplierX {
	public void multiply() {
		// declare data
		double firstNum;
		double secondNum;
		double product;
		firstNum = 10;
		secondNum = 20.5;
		
		// calculate result
		product = secondNum * firstNum;
		// output results
		System.out.print(firstNum + " * ");
		System.out.print(secondNum + " = ");
		System.out.println(product);
	}
}
